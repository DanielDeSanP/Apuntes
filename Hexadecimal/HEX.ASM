.MODEL SMALL 
.DATA
	TMPNUM DW ? ;UNA VARIABLE DE 2 A LA 16 BITS
	TMPCHR DB 8 DUP(' '),'$'
.STACK
.CODE
		;Este programa convierte un numero binario a hexadecimal.
INICIO: 
	MOV AX,@DATA
	MOV DS,AX
	MOV AX,0135H
	ADD AX,0173H
	MOV TMPNUM,AX
	MOV CX,4
CICLO:
	MOV AX,0FH
	AND AL,30H
	ADD AL,30H
	JL CONV
	ADD AL,07H
CONV:
	MOV SI,CX
	MOV TMPCHR[SI],AL

	ROR TMPNUM,1
	ROR TMPNUM,1
	ROR	TMPNUM,1
	ROR	TMPNUM,1
LOOP CICLO
EXHIBE:
	LEA DX,TMPCHR
	MOV AH,9H
	INT 21H
FIN:
	MOV AH,4CH
	INT 21H
END INICIO
