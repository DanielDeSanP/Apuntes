.MODEL SMALL
.DATA
	CADENA  DB ?
	CAR DB ?
.CODE
.STACK
INICIO:
					;DEFINICION DE PROCEDIMIENTO PRINCIPAL
	MOV AX,@DATA 	;SEG DATA ME RETORNA LA DIRECCION DEL SEGMENTO
					; Y SE LO ASIGNA A AX
	MOV DS,AX 		; Y DESPUES LO TRANSFIERO A DS
	MOV SI,0
	MOV CX,0
	INT 21H
	MOV CAR,AL
	MOV SI,0

INGRESAR:
	INT 21H
	MOV CADENA[SI],AL
	INC SI
	CMP AL,13D
	JNE INGRESAR
	
	INT 21H
	MOV CAR, AL

CHECAR:	MOV AL,CADENA[SI]
		CMP AL,CAR 
		INT 21H 
		JE CUENTA
		INC SI
		CMP AL,13D ;COMPARA SI ES EL ENTER DE LA CADENA
		JE	EXHIBE
		JMP	CHECAR

CUENTA: INC CL
		INC SI
		JMP	CHECAR

EXHIBE:	MOV	AL,CL
		ADD AL,30H
		MOV DL,AL
		MOV	AH,2H
		INT	21H
		MOV	AH,4CH
		INT 21H

END INICIO