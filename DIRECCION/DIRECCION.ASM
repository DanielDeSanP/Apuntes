;PROGRAMA QUE ENTRGA EL DIRECTIORIO DONDE SE ENCUENTRA
.MODEL SMALL
.DATA
	RUTA DB ?
	FINCAD	DB	13D, 10D, '$'
.CODE
.STACK


INICIO:
	MOV AX,@DATA
	MOV	DS,AX
	MOV AH, 19H	;OBTIENE EL DRIVE
	INT 21H
	MOV AL,[DI]
	ADD	AL,41H	;CONVIERTE A SI EQUIVALENTE EN CERO
	MOV	DL,AL
	MOV	AH,2H
	INT 21H

	MOV	DL,':'
	MOV	AH,2H
	INT	21H
	MOV	DL,'\'
	MOV	AH,2H
	INT	21H

	LEA	SI, RUTA	;SE OBTIENE EL VALOR DE LA RUTA
	MOV DL, 0		;OBTIENE LA UNIDAD ACTUAL
	MOV	AH, 47H		;RECUPERA LA RUTA
	INT 21H

DESP:
	MOV	DL,[SI]	;OBTIENE EL CONTENIDO DE LA DIRECCION DE SI EN DL
	CMP DL, 0	;VERIFICA FIN DE CADENA
	JZ FIN 		;SI ES ASI UNCUAMENTE ANEXA ENTER Y DESP DE CARRO
	MOV AH, 21H	;IMPRIME UN CARACTER
	INT 21H
	INC SI 		;OBTIENE EL SIGUIENTE
	JMP DESP	;HASTA TERMINAR
FIN:
	MOV AH,4CH
	INT 21H

END INICIO